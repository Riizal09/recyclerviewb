package com.rizalfadilah.recyclerviewb;

public class MahasiswaModel
{
    private String NIM, nama,  JenisKelamanin, JP;

    public MahasiswaModel(){
    }

    public String getNIM() {
        return NIM;
    }

    public void setNIM(String NIM) {
        this.NIM = NIM;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenisKelamanin() {
        return JenisKelamanin;
    }

    public void setJenisKelamanin(String jenisKelamanin) {
        JenisKelamanin = jenisKelamanin;
    }

    public String getJP() {
        return JP;
    }

    public void setJP(String JP) {
        this.JP = JP;
    }
}
